    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        String codigo = txtUsuario.getText();
        String password = new String(txtPassword.getPassword());
        
        Usuario u = Datos.buscarUsuario(codigo, password);
        
        if (u != null) {
            if (u instanceof Administrador) {
                System.out.println("Bienvenido Admin");
            }
            else if (u instanceof Instructor) {
                System.out.println("Bienvenido Instructor");
            }
            else if (u instanceof Estudiante) {
                System.out.println("Bienvenido Estudiante");
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Credenciales incorrectas");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

